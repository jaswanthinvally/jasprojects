import turtle as t
t.bgcolor("white")
t.pensize(10)
t.speed(5)
t.penup()
t.goto(-500,0)
t.pendown()
t.color("red")
t.begin_fill()
t.left(90)
t.forward(200)
t.right(120)
t.forward(100)
t.left(60)
t.forward(100)
t.right(120)
t.forward(200)
t.penup()
t.goto(-200,0)
t.pendown()
for i in range(50):
 t.circle(20)
 t.circle(20*i)
 t.done()